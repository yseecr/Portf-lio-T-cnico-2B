public class Genero{
  private String rock;
  private String sertanejo;
  private String pagode;
  private String samba;
  private String bossa_nova;
  private String pop;
  private String indie;

  public Genero(String rock, String sertanejo, String pagode, String samba, String bossa_nova, String pop, String indie){
    this.rock = rock;
    this.sertanejo = sertanejo;
    this.pagode = pagode;
    this.samba = samba;
    this.bossa_nova = bossa_nova;
    this.pop = pop;
    this.indie = indie;
  }
}
